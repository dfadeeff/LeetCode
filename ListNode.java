package CrashCourse;

public class ListNode {
    public int val;
    public CrashCourse.ListNode next;

    ListNode() {
    }

    public ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, CrashCourse.ListNode next) {
        this.val = val;
        this.next = next;
    }
}



